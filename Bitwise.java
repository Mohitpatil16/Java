package JavaOperators;

public class Bitwise {
		public int a = 60;
		public int b = 13;
		
		public void printResult() {
			System.out.println("a = 60, b = 13");
			System.out.println("a & b: " + (a & b));
			System.out.println("a | b: " + (a | b));
			System.out.println("a ^ b: " + (a ^ b));
			System.out.println("a<<2: " + (a<<2));
			System.out.println("a>>2: " + (a>>2));
		}	
}
