import java.util.Scanner;

public class Pattern_Question2 {
    public static void main(String[] args){
        //Scanner sc = new Scanner(System.in);
        int n=4;
        int m=5;
        //Outer Loop
        for(int i = 1; i <=n;i++){
            // inner loop
            for(int j=1; j<=m ; j++){
                System.out.print("*");
            }System.out.println();
        }
    }

}
